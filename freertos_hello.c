/*******************************************************************************
 * Definitions
 ******************************************************************************/

/* Task priorities. */
#define hello_task_PRIORITY (configMAX_PRIORITIES - 1)
/*******************************************************************************
 * Prototypes
 ******************************************************************************/
static void hello_task(void *pvParameters);
//extern void SystemInitHook(void);
extern int Atest(void);
/*******************************************************************************
 * Code
 ******************************************************************************/

#define CONFIG_CPLUSPLUS
void nSystemInitHook(void)
{
#ifdef CONFIG_CPLUSPLUS
    extern void __do_global_ctors_aux(void);
    extern void __do_init_array_aux(void);
    __do_global_ctors_aux();
    __do_init_array_aux();
#endif
}

/*!
 * @brief Application entry point.
 */
int main(void)
{
    /* Init board hardware. */
    BOARD_ConfigMPU();
    BOARD_InitPins();
    BOARD_BootClockRUN();
    BOARD_InitDebugConsole();

    Atest();

    if (xTaskCreate(hello_task, "Hello_task", configMINIMAL_STACK_SIZE + 10, NULL, hello_task_PRIORITY, NULL) != pdPASS)
    {
        PRINTF("Task creation failed!.\r\n");
        while (1)
            ;
    }
    vTaskStartScheduler();
    for (;;)
        ;
}

/*!
 * @brief Task responsible for printing of "Hello world." message.
 */
static void hello_task(void *pvParameters)
{
    for (;;)
    {
        PRINTF("Hello world.\r\n");
	Atest();
        //vTaskSuspend(NULL);
        vTaskDelay(800);
    }
}
